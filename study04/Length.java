package cn.legeyung.study04;

/**
 * 数组的长度学习
 * 
 * @author 雍珑庚(Lege_Yung)
 * 
 */
public class Length {
	public static void main(String[] args) {
		String str = "第一队列";
		String st = "第二队列";

		int i = str.length();
		int j = st.length();

		System.out.println("第一队列的长度是：" + i);
		System.out.println("第二队列的长度是：" + j);
	}
}
