package cn.legeyung.study02;

/**
 * boolean的一些运算
 * 
 * @author 雍珑庚(Lege_Yung)
 * 
 */
public class Validate {
	public static void main(String[] args) {
		System.out.println("5>4 的值是：" + (5 > 4));
		System.out.println("5<4 的值是：" + (5 < 4));
		System.out.println("5>=4 的值是：" + (5 >= 4));
		System.out.println("5<=4 的值是：" + (5 <= 4));
		System.out.println("5==4 的值是：" + (5 == 4));
		System.out.println("5!=4 的值是：" + (5 != 4));
	}
}
