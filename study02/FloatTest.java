package cn.legeyung.study02;

/**
 * float学习
 * 
 * @author 雍珑庚(Lege_Yung)
 * 
 */
public class FloatTest {
	public static void main(String[] args) {
		// 声明两个变量
		float f;
		double d;

		// 分别为两个变量赋值
		f = 12.11f;
		d = 1.001;

		// 分别将两个值打印输出
		System.out.println("float型的变量f的值是：" + f);
		System.out.println("double型变量d的值是：" + d);
	}
}
