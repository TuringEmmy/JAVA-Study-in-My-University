package cn.legeyung.study05;

/**
 * for循环的再次使用
 * 
 * @author 雍珑庚(Lege_Yung)
 * 
 */
public class ForTwoTest {
	public static void main(String[] args) {
		for (int i = 1; i < 2; i++)
			System.out.println("这是第一个循环中的i，i=" + i);
		for (int i = 2; i < 3; i++)
			System.out.println("这是第二个循环中的i，i=" + i);
	}
}
