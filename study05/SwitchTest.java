package cn.legeyung.study05;

/**
 * switch case 的学习
 * 
 * @author 雍珑庚(Lege_Yung)
 * 
 */
public class SwitchTest {
	public static void main(String[] args) {
		int i = 1;

		switch (i) {
		case 1:
			System.out.println("今天要去数学辅导班");
			break;
		case 2:
			System.out.println("今天要去语文辅导班");
			break;
		case 3:
			System.out.println("今天要去英语辅导班");
			break;
		case 4:
			System.out.println("今天要去物理辅导班");
			break;
		case 5:
			System.out.println("今天要去化学辅导班");
			break;
		case 6:
			System.out.println("今天要去生物辅导班");
			break;
		case 7:
			System.out.println("今天要去政治辅导班");
			break;
		}
	}
}
